/**
 * 将 vendor/auto-redbook-themes/<id>.css 合并为 src/xhsAutoRedbookThemeRaw.generated.ts
 * 来源：https://github.com/comeonzhj/Auto-Redbook-Skills/tree/main/assets/themes
 * 在仓库内执行: node scripts/merge-xhs-auto-redbook-css.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const dir = path.join(root, "vendor", "auto-redbook-themes");
const ids = [
	"default",
	"playful-geometric",
	"neo-brutalism",
	"botanical",
	"professional",
	"retro",
	"terminal",
	"sketch",
];

function esc(s) {
	return s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

let out =
	"// AUTO-GENERATED by scripts/merge-xhs-auto-redbook-css.mjs — do not edit by hand\n\n";
out += "export const AUTO_REDBOOK_THEME_RAW: Record<string, string> = {\n";

for (const id of ids) {
	const fp = path.join(dir, `${id}.css`);
	if (!fs.existsSync(fp)) {
		console.error("Missing:", fp);
		process.exit(1);
	}
	let raw = fs.readFileSync(fp, "utf8");
	/* 与插件 DOM 一致：上游用 .card-inner / .card-content */
	raw = raw.replace(/\.card-inner\b/g, ".mdtp-xhs-card-inner");
	raw = raw.replace(/\.card-content\b/g, ".mdtp-xhs-card-content");
	out += `\t${JSON.stringify(id)}: \`${esc(raw)}\`,\n`;
}
out += "};\n";

const dest = path.join(root, "src", "xhsAutoRedbookThemeRaw.generated.ts");
fs.writeFileSync(dest, out, "utf8");
console.log("Wrote", dest, "bytes", Buffer.byteLength(out));
